package Review2;

public class Double1 {
	public static void main(String[] args) {
		
		
	int b = 7;
		
		for(int i=0; i<b; i++) {
				System.out.print(i++);
				System.out.print(" ");
				
				
	}
		
		System.out.println();
		for(int i=0; i<b; ++i) {
			System.out.print(++i);
			System.out.print(" ");
			
			
}
	
	}
}
	
