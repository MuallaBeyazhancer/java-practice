package review1;

public class StringsConcat {
	public static void main(String[] args) {
		
		String firstName = "Mualla";
		System.out.println("My name is " + firstName);

		String city = "Izmir";
		System.out.println("I am from " + city);

		String job = "SDET";
		String company = "Department of Defense";
		System.out.println("I work as " + job + " in " + company);

		int zipcode = 20152;
		System.out.println("I live in " + zipcode + " zipcode.");
	}

}
